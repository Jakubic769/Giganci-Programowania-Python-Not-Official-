#2. Za pomocą funkcji len() sprawdź długości poszczególnych obiektów

tekst = "Python"
print(len(tekst))  # wynik: 6

lista = [1, 2, 3, 4]
print(len(lista))  # wynik: 4

krotka = (10, 20, 30)
print(len(krotka))  # wynik: 3

slownik = {'a': 1, 'b': 2}
print(len(slownik))  # wynik: 2 

zbior = {1, 2, 3, 3, 2}
print(len(zbior))  # wynik: 3
